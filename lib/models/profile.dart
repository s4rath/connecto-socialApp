
class Profile {
  final String username;
  final String fullName;
  final String bio;
  final String profileImage;
  final int posts;
  final int followers;
  final int following;

  Profile({
    required this.username,
    required this.fullName,
    required this.bio,
    required this.profileImage,
    required this.posts,
    required this.followers,
    required this.following,
  });
}


